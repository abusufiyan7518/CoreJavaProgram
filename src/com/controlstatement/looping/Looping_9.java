package com.controlstatement.looping;

public class Looping_9 {

	public static void main(String[] args) {
		int a = 40;
		int b = 35;
		int c = a + b;
		System.out.println(c);
		System.out.println();
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
			System.out.println();

		}

	}

}
